class Object {
public:
	mat4 model;
    unsigned int irradianceMap;
    unsigned int prefiltermap;
    unsigned int brdfMap;
    shared_ptr<myTexture> texture;
    virtual void render(Shader& shader) {
        shader.use();
        shader.setMat4("model", model);
        shader.setInt("albedoMap", 0);
        shader.setInt("normalMap", 1);
        shader.setInt("metallicMap", 2);
        shader.setInt("roughnessMap", 3);
        shader.setInt("irradianceMap", 4);
        shader.setInt("prefilterMap", 5);
        shader.setInt("brdfLUT", 6);
        shader.setInt("shadowMap", 7);

        glActiveTexture(GL_TEXTURE0);
        glBindTexture(GL_TEXTURE_2D, texture->albedo);
        glActiveTexture(GL_TEXTURE1);
        glBindTexture(GL_TEXTURE_2D, texture->normal);
        glActiveTexture(GL_TEXTURE2);
        glBindTexture(GL_TEXTURE_2D, texture->metallic);
        glActiveTexture(GL_TEXTURE3);
        glBindTexture(GL_TEXTURE_2D, texture->roughness);/**/
        glActiveTexture(GL_TEXTURE4);
        glBindTexture(GL_TEXTURE_CUBE_MAP, irradianceMap);
        glActiveTexture(GL_TEXTURE5);
        glBindTexture(GL_TEXTURE_CUBE_MAP, prefiltermap);
        glActiveTexture(GL_TEXTURE6);
        glBindTexture(GL_TEXTURE_2D, brdfMap);


    }
    Object(shared_ptr<myTexture> Texture, mat4 M) {
        texture = Texture;
        model = glm::mat4(1.0f);
        model = M;
    }
};
